#include <stdio.h>

int main() {
    int input;
    int sum = 0;
    scanf("%d", &input);
    if (input <= 100000) {
        sum = input * 0.1;
    } else if (input <= 200000) {
        sum = 100000 * 0.1 + (input - 100000) * 0.075;
    } else if (input <= 400000) {
        sum = 100000 * 0.1 + (200000 - 100000) * 0.075 + (input - 200000) * 0.05;
    } else if (input <= 600000) {
        sum = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (input - 400000) * 0.03;
    } else if (input <= 1000000) {
        sum = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (600000 - 400000) * 0.03 + (input - 600000) * 0.015;
    } else {
        sum = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (600000 - 400000) * 0.03 + (1000000 - 600000) * 0.015 +
              (input - 1000000) * 0.01;
    }

    printf("%d", sum);
    return 0;
}
