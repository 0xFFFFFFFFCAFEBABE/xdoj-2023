#include <stdio.h>

int main() {
    double l;
    scanf("%lf", &l);
    double sum = 0;
    if (l <= 100000) {
        sum = l * 0.1;
    } else if (l <= 200000) {
        sum = 100000 * 0.1 + (l - 100000) * 0.075;
    } else if (l <= 400000) {
        sum = 100000 * 0.1 + (200000 - 100000) * 0.075 + (l - 200000) * 0.05;
    } else if (l <= 600000) {
        sum = 100000 * 0.1 + (200000 - 100000) * 0.075 + (400000 - 200000) * 0.05 + 0.03 * (l - 400000);
    } else if (l <= 1000000) {
        sum = 100000 * 0.1 + (200000 - 100000) * 0.075 + (400000 - 200000) * 0.05 + 0.03 * (600000 - 400000) +
              0.015 * (l - 600000);
    } else {
        sum = 100000 * 0.1 + (200000 - 100000) * 0.075 + (400000 - 200000) * 0.05 + 0.03 * (600000 - 400000) +
              0.015 * (1000000 - 600000) + (l - 1000000) * 0.01;
    }

    printf("%.2f", sum);
    return 0;
}